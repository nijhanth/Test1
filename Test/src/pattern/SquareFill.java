package pattern;

public class SquareFill {

	public static void main(String[] args) {
		
		int row =5;
		int clm =15;
		
		for(int countr =1; countr<=row; countr++) {
			
			for(int countc=1; countc<=clm; countc++) {
			
			System.out.print('*');
			
			}
			System.out.println(" ");
		}

	}

}
